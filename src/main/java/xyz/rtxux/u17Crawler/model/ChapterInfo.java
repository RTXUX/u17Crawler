package xyz.rtxux.u17Crawler.model;

public class ChapterInfo {
    private int ID;
    private int ComicID;
    private String Name;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getComicID() {
        return ComicID;
    }

    public void setComicID(int comicID) {
        ComicID = comicID;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
